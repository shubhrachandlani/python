# Integer
integer_number = 10
print("Integer Number:", integer_number, "Type:", type(integer_number))

# Floating-point number
float_number = 3.14
print("Float Number:", float_number, "Type:", type(float_number))

# Complex number
complex_number = 2 + 3j
print("Complex Number:", complex_number, "Type:", type(complex_number))
